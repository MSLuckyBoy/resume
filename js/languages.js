    $("lang_uz").textContent = sLang.lang_progress.uz;
    $("lang_ru").textContent = sLang.lang_progress.ru;
    $("lang_en").textContent = sLang.lang_progress.en;
    $("about_title").textContent = sLang.about_me.title;
    $("about_text").innerHTML = sLang.about_me.text;

    let exp = sLang.experience.jobs;
    $("exp_title").textContent = sLang.experience.title;
    $("exp-1-1").textContent = exp[0].organization;
    $("exp-1-2").textContent = exp[0].date;
    $("exp-1-3").textContent = exp[0].job_title;
    $("exp-1-4").textContent = exp[0].description;
    $("exp-2-1").textContent = exp[1].organization;
    $("exp-2-2").textContent = exp[1].date;
    $("exp-2-3").textContent = exp[1].job_title;
    $("exp-2-4").textContent = exp[1].description;

    let edu = sLang.education.educations;
    $("edu_title").textContent = sLang.education.title;
    $("edu-1-1").textContent = edu[0].name;
    $("edu-1-2").textContent = edu[0].date;
    $("edu-1-3").textContent = edu[0].direction;
